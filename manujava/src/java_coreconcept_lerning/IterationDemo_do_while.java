package java_coreconcept_lerning;

public class IterationDemo_do_while {
// while ,do while , for ,for each 
	public static void main(String[] args) {
		// if condition fails even you want to execute statement atleast once  then we choose do while loop
		
		
		int i=1; // initialization
		do // if condition fails do atleast once 
			{
      System.out.println("hello: "+i);
      i++;// increment or decrement
      
		}while(i<=5);// condition
	
	}

}
