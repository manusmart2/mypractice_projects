package java_coreconcept_lerning;

public class SwitchCase {

	public static void main(String[] args) {
		
		
		int n=3;
		//char n='a';
		//double n=5.5; // double cant support switch
		//String n="mnan";// below 1.7 version not support 
		
		
		switch(n) {
		case 1:
		System.out.println("one");
		break;
		case 2:
		System.out.println("two");
		break;
		case 3:
		System.out.println("three");
		break;
		case 4:
		System.out.println("foure");
		break;
		case 5:
		System.out.println("five");
		break;
		}
	}

	
	
}
